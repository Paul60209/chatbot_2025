-- Drop existing table if it exists
DROP TABLE IF EXISTS sales;

-- Create the sales table
CREATE TABLE sales (
    ID VARCHAR(10) PRIMARY KEY,
    Date DATE,
    Region VARCHAR(50),
    City VARCHAR(50),
    Category VARCHAR(50),
    Product VARCHAR(50),
    Quantity INT,
    Unit_Price DECIMAL(10,2),
    Total_Price DECIMAL(10,2)
);

